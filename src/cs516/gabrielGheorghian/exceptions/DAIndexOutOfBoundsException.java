public class DAIndexOutOfBoundsException extends Exception {

	public DAIndexOutOfBoundsException() {
	}

	public DAIndexOutOfBoundsException(String message) {
		super(message);
	}

	public DAIndexOutOfBoundsException(Throwable cause) {
		super(cause);
	}

	public DAIndexOutOfBoundsException(String message, Throwable cause) {
		super(message, cause);
	}

}